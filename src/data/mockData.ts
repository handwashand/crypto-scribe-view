// Helper to create parsed signal
const createParsedSignal = (params: {
  channelName: string;
  messageLink: string;
  publishedAt: string;
  symbol: string;
  side: 'LONG' | 'SHORT';
  leverage: number;
  entryType: 'MARKET' | 'LIMIT' | 'RANGE';
  entryPrice?: number;
  entryRange?: { low: number; high: number };
  takeProfits: number[];
  stopLoss: number;
  hardSl?: boolean;
  status: 'ACTIVE' | 'CLOSED' | 'EXPIRED';
  confidence?: number;
  notes?: string;
}): ParsedSignal => ({
  source: {
    channelName: params.channelName,
    messageLink: params.messageLink,
    publishedAtText: params.publishedAt,
  },
  signal: {
    symbolRaw: params.symbol,
    market: 'USDT_PERP',
    side: params.side,
    leverageX: params.leverage,
    entry: {
      type: params.entryType,
      price: params.entryPrice ?? null,
      range: params.entryRange ?? null,
    },
    takeProfits: params.takeProfits.map(price => ({ price, portion: null })),
    stopLoss: {
      price: params.stopLoss,
      hard: params.hardSl ?? true,
    },
    status: params.status,
    author: null,
    updateOfId: null,
    externalId: null,
  },
  meta: {
    confidence: params.confidence ?? 0.92,
    notes: params.notes ?? 'Signal parsed automatically.',
  },
  version: 'sig-schema-1.0',
});

